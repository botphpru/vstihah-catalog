<?php

namespace App\Services;

use App\Core\Exceptions\LogicException;

class Prompt
{
    public static function getPromptForGenPoem(array $values): string
    {
        // === Валидация (без изменений) ===
        if (count($values) < 2) {
            throw new LogicException(
                'Неправильная логика, почему для генерации стиха пришло меньше значений: ' . json_encode($values)
            );
        }

        if (!isset($values['quatrains']) || !is_int($values['quatrains']) || $values['quatrains'] < 1) {
            throw new LogicException('Поле quatrains обязательно и должно быть положительным целым числом');
        }

        // === Сборка промпта ===
        $lines = [];
        $lines[] = "Ты — талантливый поэт. Сочини поздравительное стихотворение на русском языке.";

        // Размер
        $quatrains = $values['quatrains'];
        $lines[] = "Ровно {$quatrains} " . self::pluralize($quatrains, 'четверостишие', 'четверостишия', 'четверостиший') . " (каждое — 4 строки).";

        // Событие (оставляем как есть — это тема, а не форма обращения)
        if (!empty($values['event'])) {
            $lines[] = "Повод: «{$values['event']}». Раскрой эту тему в стихотворении.";
        }

        // Жанр/стиль (чуть мягче формулировка)
        if (!empty($values['genre'])) {
            $lines[] = "Жанр: {$values['genre']}. Выдержи эту тональность в стихотворении.";
        }

        // 🔥 Получатель + Имя — ГИБКАЯ ФОРМУЛИРОВКА 🔥
        if (!empty($values['recipient']) && !empty($values['name'])) {
            $lines[] = "Стихотворение предназначено для {$values['recipient']} по имени {$values['name']}. " .
                "Ты можешь обращаться к этому человеку творчески: использовать имя, роль, их комбинацию или намёк — как тебе подсказывает вдохновение. " .
                "Главное — чтобы было понятно, кому адресовано поздравление.";
        }
        // Только получатель (без имени)
        elseif (!empty($values['recipient'])) {
            $lines[] = "Стихотворение предназначено для {$values['recipient']}. " .
                "Обращайся к этому человеку уважительно и уместно, но не обязательно использовать слово «{$values['recipient']}» буквально.";
        }
        // Только имя (без получателя)
        elseif (!empty($values['name'])) {
            $lines[] = "В стихотворении должно упоминаться имя «{$values['name']}». " .
                "Используй его естественно: в обращении, в тексте или как часть образа — как тебе удобнее.";
        }

        // Требования к качеству (чуть расширили)
        $lines[] = "Требования:";
        $lines[] = "- Оригинальное стихотворение, без клише и шаблонов;";
        $lines[] = "- Естественная рифма, чёткий ритм, лёгкость чтения;";
        $lines[] = "- Текст полностью на русском языке;";
        $lines[] = "- Тёплые, искренние, персонализированные пожелания;";
        $lines[] = "- Избегай буквальных конструкций вида «Роль Имя» (например, «Коллега Яна») — обращайся творчески.";

        // Если нет события — уточняем
        if (empty($values['event'])) {
            $lines[] = "- Универсальное поздравление (без привязки к конкретному празднику).";
        }

        $lines[] = "Выведи только готовое стихотворение, без заголовка, кавычек, пояснений и служебных символов.";

        return implode("\n", $lines);
    }

    /**
     * Склонение существительных для чисел
     */
    private static function pluralize(int $number, string $one, string $two, string $five): string
    {
        $n = abs($number) % 100;
        $n1 = $n % 10;

        if ($n > 10 && $n < 20) {
            return $five;
        }
        if ($n1 > 1 && $n1 < 5) {
            return $two;
        }
        if ($n1 === 1) {
            return $one;
        }
        return $five;
    }
}